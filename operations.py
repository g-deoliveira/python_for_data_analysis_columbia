x = 10
y = 3

# exponentiation
assert 1000 == x ** y

# multiplication
assert 30 == x * y

# division
assert 3 + 1 / 3 == x / y

# note, division returns a float
assert 10 / 2 == 5.0

# floot division
assert 3 == x // y

# addition
assert x + y == 13

# subtraction
assert x - y == 7

# modulus
assert x % y == 1
